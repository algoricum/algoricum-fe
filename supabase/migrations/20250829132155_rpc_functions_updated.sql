CREATE OR REPLACE FUNCTION public.get_clinic_integrations_with_status(clinic_uuid uuid)
 RETURNS TABLE(integration_id uuid, integration_name text, integration_type integration_type, auth_type integration_auth_type, connected boolean, connection_id uuid, connection_status connection_status, auth_data jsonb, expires_at timestamp with time zone, last_sync_at timestamp with time zone, created_at timestamp with time zone, updated_at timestamp with time zone, integration_logo text, description text)
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
BEGIN
  RETURN QUERY
  SELECT 
    i.id as integration_id,
    i.name as integration_name,
    i.type as integration_type,
    i.auth_type,
    CASE 
      WHEN ic.id IS NOT NULL AND ic.status = 'active' THEN TRUE
      ELSE FALSE
    END as connected,
    ic.id as connection_id,
    ic.status as connection_status,
    ic.auth_data,
    ic.expires_at,
    ic.updated_at as last_sync_at,
    i.created_at,
    i.updated_at,
    i.integration_logo,
    i.description
  FROM integrations i
  LEFT JOIN integration_connections ic 
    ON i.id = ic.integration_id 
    AND ic.clinic_id = clinic_uuid
  ORDER BY i.name ASC;
END;
$function$
;

